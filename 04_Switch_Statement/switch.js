const month = "march"
switch (month) {
    case "jan":
        console.log("January");
        break;
    case "fab":
        console.log("Feb");
        break;
    case "march":
        console.log("March");
        break;
    case "april":
        console.log("April");
        break;

    default:
        console.log("Default Case Match");
        break;
}
